package net.jeeshop.services.front.alipay.bean;

import java.io.Serializable;
import java.util.Date;

import net.jeeshop.core.dao.QueryModel;

public class AliPay extends QueryModel implements Serializable {
	
	private static final long serialVersionUID = 1L;
	private String id; 
	private String callback;// 回调方法名 
	private String outTradeNo;// 平台流水号（orderpay表的remark字段）
	private String tradeNo;// 支付宝流水号
	private String tradeStatus;// 支付响应状态 
	private String params;// 支付响应信息
	private Date date;// 操作时间
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCallback() {
		return callback;
	}
	public void setCallback(String callback) {
		this.callback = callback;
	}
	public String getOutTradeNo() {
		return outTradeNo;
	}
	public void setOutTradeNo(String outTradeNo) {
		this.outTradeNo = outTradeNo;
	}
	public String getTradeNo() {
		return tradeNo;
	}
	public void setTradeNo(String tradeNo) {
		this.tradeNo = tradeNo;
	}
	public String getTradeStatus() {
		return tradeStatus;
	}
	public void setTradeStatus(String tradeStatus) {
		this.tradeStatus = tradeStatus;
	}
	public String getParams() {
		return params;
	}
	public void setParams(String params) {
		this.params = params;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	} 
	
}
