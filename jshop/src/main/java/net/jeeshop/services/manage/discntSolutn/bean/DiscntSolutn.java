package net.jeeshop.services.manage.discntSolutn.bean;

import java.io.Serializable;

import net.jeeshop.core.dao.QueryModel;

/**
 * 折扣方案
 * @author Maryn
 *
 */
@SuppressWarnings("serial")
public class DiscntSolutn extends QueryModel implements Serializable {
	private String id;
	private String name;
	private String updateTime;// 更新时间
	private String updatePerson;// 更新操作的人
	private String status;// 是否有效
	private String comment;// 备注
	
	public void clear(){
		super.clear();
		this.id = null;
		this.name = null;
		this.updateTime = null;
		this.updatePerson = null;
		this.status = null;
		this.comment = null;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getUpdatePerson() {
		return updatePerson;
	}
	public void setUpdatePerson(String updatePerson) {
		this.updatePerson = updatePerson;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	
}
