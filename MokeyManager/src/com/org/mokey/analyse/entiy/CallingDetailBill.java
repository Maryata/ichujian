public class CallingDetailBill {
    private String phone;//手机号
    private String date;//日期
    private String fyCode;//飞语账号
    private String ekStartTime;//开始时间
    private String ekEndTime;//结束时间
    private Long ekTime;//时长
    public Long getEkTime() {
        return ekTime;
    }

    public void setEkTime(Long ekTime) {
        this.ekTime = ekTime;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getFyCode() {
        return fyCode;
    }

    public void setFyCode(String fyCode) {
        this.fyCode = fyCode;
    }

    public String getEkStartTime() {
        return ekStartTime;
    }

    public void setEkStartTime(String ekStartTime) {
        this.ekStartTime = ekStartTime;
    }

    public String getEkEndTime() {
        return ekEndTime;
    }

    public void setEkEndTime(String ekEndTime) {
        this.ekEndTime = ekEndTime;
    }



}
