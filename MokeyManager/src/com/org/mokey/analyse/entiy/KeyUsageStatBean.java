package com.org.mokey.analyse.entiy;

public class KeyUsageStatBean {
	
	private int startCn;//启动
	private String type;//操作类别
	private String key;//key
	
	private String day;//日期
	//private String startRate ="0.00";

	public int getStartCn() {
		return startCn;
	}

	public void setStartCn(int startCn) {
		this.startCn = startCn;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}
	
}
