package com.org.mokey.analyse.vo;

/**
 * e键-综合数据统计
 *
 * Created by Maryn on 2016/4/5.
 */
public class EkeyComplexBean {

    private String time;// 时间

    private long count_download = 0;// 下载

    private long count_activation = 0;// 激活

    private long count_register = 0;// 注册

    private long count_active = 0;// 活跃

    private long count_staying = 0;// 留存

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public long getCount_download() {
        return count_download;
    }

    public void setCount_download(long count_download) {
        this.count_download = count_download;
    }

    public long getCount_activation() {
        return count_activation;
    }

    public void setCount_activation(long count_activation) {
        this.count_activation = count_activation;
    }

    public long getCount_register() {
        return count_register;
    }

    public void setCount_register(long count_register) {
        this.count_register = count_register;
    }

    public long getCount_active() {
        return count_active;
    }

    public void setCount_active(long count_active) {
        this.count_active = count_active;
    }

    public long getCount_staying() {
        return count_staying;
    }

    public void setCount_staying(long count_staying) {
        this.count_staying = count_staying;
    }
}
