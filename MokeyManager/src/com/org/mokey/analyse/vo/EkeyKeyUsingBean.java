package com.org.mokey.analyse.vo;

/**
 * e键-按键使用情况统计
 *
 * Created by Maryn on 2016/4/5.
 */
public class EkeyKeyUsingBean {

    private String timeStr;// 时间

    private long actCount = 0L;// 激活用户数

    private EkeyKeyBean keyBean_start = new EkeyKeyBean();// 启动

    private EkeyKeyBean keyBean_1st = new EkeyKeyBean();// 1号键

    private EkeyKeyBean keyBean_2nd = new EkeyKeyBean();// 2号键

    private EkeyKeyBean keyBean_3rd = new EkeyKeyBean();// 3号键

    private EkeyKeyBean keyBean_4th = new EkeyKeyBean();// 4号键

    private EkeyKeyBean keyBean_sum = new EkeyKeyBean();// 合计

    public String getTimeStr() {
        return timeStr;
    }

    public void setTimeStr(String timeStr) {
        this.timeStr = timeStr;
    }

    public EkeyKeyBean getKeyBean_start() {
        return keyBean_start;
    }

    public void setKeyBean_start(EkeyKeyBean keyBean_start) {
        this.keyBean_start = keyBean_start;
    }

    public long getActCount() {
        return actCount;
    }

    public void setActCount(long actCount) {
        this.actCount = actCount;
    }

    public EkeyKeyBean getKeyBean_1st() {
        return keyBean_1st;
    }

    public void setKeyBean_1st(EkeyKeyBean keyBean_1st) {
        this.keyBean_1st = keyBean_1st;
    }

    public EkeyKeyBean getKeyBean_2nd() {
        return keyBean_2nd;
    }

    public void setKeyBean_2nd(EkeyKeyBean keyBean_2nd) {
        this.keyBean_2nd = keyBean_2nd;
    }

    public EkeyKeyBean getKeyBean_3rd() {
        return keyBean_3rd;
    }

    public void setKeyBean_3rd(EkeyKeyBean keyBean_3rd) {
        this.keyBean_3rd = keyBean_3rd;
    }

    public EkeyKeyBean getKeyBean_4th() {
        return keyBean_4th;
    }

    public void setKeyBean_4th(EkeyKeyBean keyBean_4th) {
        this.keyBean_4th = keyBean_4th;
    }

    public EkeyKeyBean getKeyBean_sum() {
        return keyBean_sum;
    }

    public void setKeyBean_sum(EkeyKeyBean keyBean_sum) {
        this.keyBean_sum = keyBean_sum;
    }
}
